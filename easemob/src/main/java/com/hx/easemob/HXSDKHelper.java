    private EMOptions initChatOptions() {
        Log.d(TAG, "init HuanXin Options");

        EMOptions options = new EMOptions();
        // set if accept the invitation automatically
        options.setAcceptInvitationAlways(false);
        // set if you need read ack
        options.setRequireAck(true);
        // set if you need delivery ack
        options.setRequireDeliveryAck(false);

        //you need apply & set your own id if you want to use google cloud messaging.
        options.setGCMNumber("324169311137");
        //you need apply & set your own id if you want to use Mi push notification
        options.setMipushConfig("2882303761517426801", "5381742660801");
        //you need apply & set your own id if you want to use Huawei push notification
        options.setHuaweiPushAppId("10492024");

        //set custom servers, commonly used in private deployment
        if (hxSDKModel.isCustomServerEnable() && hxSDKModel.getRestServer() != null && hxSDKModel.getIMServer() != null) {
            options.setRestServer(hxSDKModel.getRestServer());
            options.setIMServer(hxSDKModel.getIMServer());
            if (hxSDKModel.getIMServer().contains(":")) {
                options.setIMServer(hxSDKModel.getIMServer().split(":")[0]);
                options.setImPort(Integer.valueOf(hxSDKModel.getIMServer().split(":")[1]));
            }
        }

        if (hxSDKModel.isCustomAppkeyEnabled() && hxSDKModel.getCutomAppkey() != null && !hxSDKModel.getCutomAppkey().isEmpty()) {
            options.setAppKey(hxSDKModel.getCutomAppkey());
        }

        options.allowChatroomOwnerLeave(getModel().isChatroomOwnerLeaveAllowed());
        options.setDeleteMessagesAsExitGroup(getModel().isDeleteMessagesAsExitGroup());
        options.setAutoAcceptGroupInvitation(getModel().isAutoAcceptGroupInvitation());

        return options;
    }

